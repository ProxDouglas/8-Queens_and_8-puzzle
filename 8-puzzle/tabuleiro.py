
class Tabuleiro:

    def __init__(self, tabuleiro_len, tabuleiro_perimetro):
        self.tabuleiro_len = tabuleiro_len
        self.tabuleiro_perimetro = tabuleiro_perimetro


    def getTabuleiro_len(self):
        return self.tabuleiro_len

    def setTabuleiro_len(self, tabuleiro_len):
        self.tabuleiro_len = tabuleiro_len

    def getTabuleiro_perimetro(self):
        return self.tabuleiro_perimetro

    def setTabuleiro_perimetro(self, tabuleiro_perimetro):
        self.tabuleiro_perimetro = tabuleiro_perimetro
